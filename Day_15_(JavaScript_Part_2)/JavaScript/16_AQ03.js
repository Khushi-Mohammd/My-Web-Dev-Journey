let quarter = 5;

// switch statement.

switch (quarter) {
  case 1: // if quarter is 1 then it will print "January, February, March".
    console.log("January, February, March");
    break;
  case 2: // if quarter is 2 then it will print "April, May, June".
    console.log("April, May, June");
    break;
  case 3: // if quarter is 3 then it will print "July, August, September".
    console.log("July, August, September");
    break;
  case 4: // if quarter is 4 then it will print "October, November,
    console.log("October, November, December");
    break;
  default: // if quarter is not 1, 2, 3 or 4 then it will print "Not a valid number".
    console.log("Not a valid number");
    break;
}
