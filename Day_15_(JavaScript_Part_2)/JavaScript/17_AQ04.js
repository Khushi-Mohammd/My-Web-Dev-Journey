let str = "AGoldenString";
// if statement to check if the string starts with "A" or "a" and its length is greater than 5.
if ((str[0] === "A" || str[0] === "a") && str.length > 5) {
  console.log(`String "${str}" is a golden string`);
} else {
  console.log(`String "${str}" is not a golden string`);
}
