let size = "S";

if (size === "XL") { // if size is XL then it prints "Price is Rs. 250".
  console.log("Price is Rs. 250");
} else if (size === "L") { // if size is L then it prints "Price is Rs. 200".
  console.log("Price is Rs. 200");
} else if (size === "M") { // if size is M then it prints "Price is Rs. 100".
  console.log("Price is Rs. 100");
} else if (size === "S") { // if size is S then it prints "Price is Rs. 50".
  console.log("Price is Rs. 50");
} else { // if size is not XL, L, M or S then it prints "Please choose a valid size".
  console.log("Please choose a valid size");
}