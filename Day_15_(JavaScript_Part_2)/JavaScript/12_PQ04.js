let color = 1;

switch (color) {
  case 1: // if color is 1 then it will print "Monday".
    console.log("Monday");
    break;
  case 2: // if color is 2 then it will print "Tuesday".
    console.log("Tuesday");
    break;
  case 3: // if color is 3 then it will print "Wednesday".
    console.log("Wednesday");
    break;
  case 4: // if color is 4 then it will print "Thursday".
    console.log("Thursday");
    break;
  case 5: // if color is 5 then it will print "Friday".
    console.log("Friday");
    break;
  case 6: // if color is 6 then it will print "Saturday".
    console.log("Saturday");
    break;
  case 7: // if color is 7 then it will print "Sunday".
    console.log("Sunday");
    break;
  default: // if color is not 1, 2, 3, 4, 5, 6 or 7 then it will print "Not a valid number
    console.log("Not a valid number");
}
