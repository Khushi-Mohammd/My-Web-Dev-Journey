let color = "white";

if (color === "red") { // if color is red then this block will execute.
  console.log("Stop! Light is Red");
} else if (color === "yellow") { // if color is yellow then this block will execute.
  console.log("Slow Down! Light is Yellow");
} else if (color === "green") { // if color is green then this block will execute.
  console.log("Go. Light is Green");
} else { // if color is not red, yellow or green then this block will execute.
  console.log("Light is Broken");
}